    <h3>McCafé</h3>
    <p>
        McCafé est similaire à Starbucks : c’est une chaîne de café qui plaît créée par l’entreprise de restauration rapide McDonald’s qui depuis 2004 ne cesse de s’étendre dans le monde. Nous pouvons nous rendre compte que McCafé est en quelque sorte une copie conforme de Starbucks ; il propose les mêmes produits (boissons et pâtisseries). De plus, chaque McCafé adapte ces produits en fonction du pays dans lequel il se trouve, ainsi de nombreuses boissons font leur entrée comme le Mocha, l’expresso, le cappuccino, l’américano ou le latte. McDonald’s a créé ce concept afin de faire concurrence à Starbucks et attirer de nouveaux clients. Même si McDonald’s fait des efforts, Starbucks attire plus de clients du fait de son application dans la décoration ou encore dans les services qu’il propose et surtout grâce au fait que les salons de cafés soient plus intimes, plus conviviales. Leur point faible est que, McDonald’s est un endroit bruyant non propice pour boire un café : le consommateur ne s’y attarde pas. 
    </p>
