    <h3>Costa Coffee</h3>
    <p>
        <b>Costa Coffee</b> est une chaîne de café britannique présente dans le monde entier et possédant la seconde place du classement juste après Starbucks ; ce qui fait de lui un concurrent redoutable. Lui aussi propose des cafés communs, des viennoiseries et des pâtisseries. La chaîne s’étend dans le monde entier puisqu’elle est présente en Europe, en Asie et en Afrique. En 2018, l’entreprise américaine Coca-Cola a racheté Costa Coffee pour 4.8 milliards d’euros.
    </p>

    <h3>Tim Hortons</h3>
    <p>
        Avec le slogan « Toujours frais », <b>Tim Hortons</b> essaye tant bien que mal à se faire une place sur le marché très disputé de la restauration rapide de cafés. Pour se démarquer, l’entreprise s’est spécialisée dans le Donut (forme de beignet sucré). Comme la plupart des autres Cafés, Tim Hortons imite Starbucks, il lui fait concurrence surtout aux Etats-Unis et au Canada. 
    </p>

    <h3>Columbus</h3>
    <p>
        <b>Columbus</b> est un café qui dans sa forme et son fond imite Starbucks en proposant toutes sortes de boissons (chaudes ou froides) à base de thé, café ou jus de fruit, et de nourriture (salés ou sucrés). Columbus réalise également un espace convivial dans lequel les gens se sentent comme chez eux. Cette chaîne de café fait surtout concurrence à Starbucks en France et au Royaume-Uni. 
    </p>

    <h3>Manolya</h3>
    <p>
        <b>Manolya</b> est à la base une petite chaîne de restauration rapide créée à Strasbourg qui, par la suite se développe et créer un espace où l’on peut boire des cafés, thés, chocolat chaud, etc … en toute tranquillité. Il n’existe pour le moment qu’un Manolya café dans le monde.
    </p>

