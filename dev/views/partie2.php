    <h2 class="m-b-40">B - Starbucks un modèle contestable et contesté</h2>
    <h3>Le dumping fiscal</h3>

    <p>
        Starbucks, comme toute entreprise veut faire de l’argent et payer le moins d’impôts possible. Pour cela ils ont recours à <b>des méthodes souvent controversées : le dumping fiscal</b>. Ce qu’on appelle le dumping fiscal est le fait de vendre sur les marchés extérieurs à des prix inférieurs à ceux du marché national, ou même inférieurs au prix de revient. 
    </p>

    <p>
        L’entreprise a fait parler d’elle en 2015 : elle était accusée de <b>n’avoir payé aucun impôt</b> pendant l’année en France. En effet, Starbucks paie des redevances (taxe due en contrepartie de l'utilisation d'un service public) à une filiale aux Pays-Bas. Ce pays est connu pour être, comme le Luxembourg, le royaume de l’optimisation fiscale : ils proposent des taux d’imposition très bas sur certaines activités. Starbucks en profite très largement en France notamment, puisque sur un chiffre d’affaire de 90 millions d’euros, ils n’ont déclaré que 700.000 euros de bénéfices. Certains journalistes qualifient cela d’« <b>optimisation fiscale agressive</b> ». Starbucks n’est de loin pas à être la seule star américaine de la consommation à utiliser ces méthodes douteuses ; on peut citer Apple, Amazon, Google… 
    </p>

    <p>
        La France n’est pas le seul pays de l’Union européenne à se faire arnaquer par Starbucks. En Grande-Bretagne, sur 500 millions de chiffre d’affaire en 2011, <b>la marque n’a pas payé un penny d’impôt au fisc de Sa Majesté</b>. Troy Alstead, directeur financier Starbucks, a essayé de convaincre les parlementaires anglais de ses difficultés, en se plaignant entre autres de la lourdeur des loyers : « Nous avons des soucis de rentabilité, plaide-t-il. On y travaille. Nous ne sommes pas du tout satisfaits. Mais ça n'a rien à voir avec de l'évasion fiscale ». 
    </p>

    <p>
        Cela a évidemment fini par alerter les autorités fiscales de la plupart des pays européens ainsi que <b>la Commission européenne qui leur réclame 30 millions d'euros</b> pour « obtention d'aide illégale de la part du gouvernement des Pays-Bas ». Seulement, Starbucks possède une armée de puissants avocats fiscalistes. De plus, les Pays-Bas se rangent du côté de Starbucks et ne réclament pas l'argent.
    </p>

    <p>
        Starbucks utilise l’évasion fiscale en Europe, ce n’est plus un secret. Mais malheureusement ce n’est pas le seul point noir de l’entreprise.
    </p>
<div class="separator"></div>
    <h3>De multiples scandales dans le monde</h3>
    <p>
        Starbucks n’est pas aussi blanc que ce qu’ils veulent nous faire croire. En effet, plusieurs scandales ont éclaté et salissent son image.
    </p>

    <ul class="liste">
        <li>
            <p><b>Racisme</b> - 
Le 12 avril 2018, un employé du Starbucks de Philadelphie aux Etats-Unis appelle la police, car deux hommes noirs dans le café ne voulaient pas commander, ils « attendaient une connaissance ». Les deux hommes seront interpellés par la police. Les images de l’arrestation ont rapidement fait le tour du monde. Le public a été choqué par <b>le racisme apparent des forces de l’ordre et du personnel Starbucks</b>. Le PDG de Starbucks Kévin Johnson s’est excusé publiquement et a fermé le 29 mai plus de 8000 établissements dont 175 000 employés le temps d’une après-midi pour une formation pour corriger les préjugés racistes, promouvoir l’inclusion, prévenir les discriminations et accueillir la clientèle. Cette démarche est faite pour réparer l’image de Starbucks suite à cet incident.</p>
        </li>

        <li>
            <div class="flex-container m-b-20">
                
                <p>
                    <b>Traitement des employés</b> - La chaine Arte, le 28 août 2018 sort le documentaire « Starbucks sans filtre » qui va dénoncer les problèmes liés aux salariés Starbucks. En effet, que ce soit aux USA ou en France, les employés se sentent <b>submergés par le travail</b> et n’ont le droit qu’à peu de pauses. Les conditions sont souvent déplorables : un comptoir trop bas qui provoque des mal de dos et des horaires compliqués pour certain. Tout ça pour un salaire relativement bas (salaire le plus faible : 1305€). Luc Hermann employé Starbucks en France témoigne : « Je n’ai jamais eu un travail aussi difficile… ». 
                </p>
                <iframe frameborder="0" width="480" height="270" src="https://www.dailymotion.com/embed/video/x6syoep" allowfullscreen allow="autoplay" class="m-l-30 p-t-10"></iframe>
            </div>
        </li>
        <li>
            <p><b>Ecologie</b> - 
Dernièrement, la secrétaire d’Etat à l’Ecologie madame Brune Poirson a convoqué les responsables des chaines de restaurations rapides dont Starbucks, McDonalds et Burger King, qui sont accusés de ne pas respecter leur obligation envers l’écologie. L’objectif de la réunion et de leur rappeler leurs devoirs en matière de tri des déchets jugés insuffisants. Depuis 2016, la loi oblige toutes entreprises produisant plus de 10 tonnes déchets biodégradables par an à disposer d’un système de tri appelé « cinq flux ». Les enseignes qui ne respecteraient pas cette règle encourraient 150 000 € d’amende. Alors s’est à Starbucks de faire attention à sa surproduction de déchets non recyclable et aux clients de jouer le jeu. 
</p>
        </li>
    </ul>

    <p>
    Bien que Starbucks soit un grand producteur de café et un exemple pour beaucoup, il reste tout de même <b>un modèle contestable</b> sur certains points comme sur l’écologie, le traitement de ces employés et le comportement de certains.
    </p>
